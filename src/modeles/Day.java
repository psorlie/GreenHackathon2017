package modeles;

public class Day {
	private int opening;
	private int closing;
	
	public int getOpening() {
		return this.opening;
	}
	
	public int getClosing() {
		return this.closing;
	}
	
	public void setOpening(int opening) {
		this.opening = opening;
	}
	
	public void setClosing(int closing) {
		this.closing = closing;
	}
}
